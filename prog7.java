// Print even numbers from 1 to 30 then odd number from 1 to 30 using 2 while loops
// variables i & j , increment & decrement concept is used

public class prog7 
{
    public static void main(String[] args) 
    {
        int i=2;
        while(i<=30)
        {
            System.out.print(i + " ");
            i+=2;
        }
        System.out.println(" ");
        int j=1;
        while(j<=30)
        {
            System.out.print(j+ " ");
            j+=2;
        }
    }
}
